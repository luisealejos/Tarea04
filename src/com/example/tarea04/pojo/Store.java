package com.example.tarea04.pojo;

public class Store {
	String Nombre;
	String Descripcion;
	String Direccion;
	String Telefono;
	String Horarios;
	String Website;
	String Email;
	String Listado_comentarios;
	int Numero_favoritos;
	String Ubicacion_geografica;
	
	public String getNombre() {
		return Nombre;
	}
	public void setNombre(String nombre) {
		Nombre = nombre;
	}
	public String getDescripcion() {
		return Descripcion;
	}
	public void setDescripcion(String descripcion) {
		Descripcion = descripcion;
	}
	public String getDireccion() {
		return Direccion;
	}
	public void setDireccion(String direccion) {
		Direccion = direccion;
	}
	public String getTelefono() {
		return Telefono;
	}
	public void setTelefono(String telefono) {
		Telefono = telefono;
	}
	public String getHorarios() {
		return Horarios;
	}
	public void setHorarios(String horarios) {
		Horarios = horarios;
	}
	public String getWebsite() {
		return Website;
	}
	public void setWebsite(String website) {
		Website = website;
	}
	public String getEmail() {
		return Email;
	}
	public void setEmail(String email) {
		Email = email;
	}
	public String getListado_comentarios() {
		return Listado_comentarios;
	}
	public void setListado_comentarios(String listado_comentarios) {
		Listado_comentarios = listado_comentarios;
	}
	public int getNumero_favoritos() {
		return Numero_favoritos;
	}
	public void setNumero_favoritos(int numero_favoritos) {
		Numero_favoritos = numero_favoritos;
	}
	public String getUbicacion_geografica() {
		return Ubicacion_geografica;
	}
	public void setUbicacion_geografica(String ubicacion_geografica) {
		Ubicacion_geografica = ubicacion_geografica;
	}
	
	
}
